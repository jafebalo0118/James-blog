def admin_only(f):
    @wraps(f)
    def decorated_function(*args, **kwargs):
        # If id is not 1 then return abort with 403 error
        if current_user.id != 1:
            return abort(403)
        # Otherwise continue with the route function
        return f(*args, **kwargs)

    return decorated_function


# TODO: Use Werkzeug to hash the user's password when creating a new user.
@app.route('/register', methods=["GET", "POST"])
def register():
    register_form = RegisterForm()
    if register_form.validate_on_submit():
        email = register_form.email.data
        check_email = db.session.execute(db.select(User).where(User.email == email)).scalar()
        if check_email:
            flash(f"The email {email} have been registered already, try Log in instead")
            return redirect(url_for("login"))
        else:
            hashed_password = generate_password_hash(password=register_form.password.data, method='pbkdf2:sha256',
                                                     salt_length=8)
            new_user = User(
                email=email,
                password=hashed_password,
                name=register_form.name.data
            )
            db.session.add(new_user)
            db.session.commit()
            login_user(new_user)
            return redirect(url_for("get_all_posts"))
    return render_template("register.html", form=register_form)


